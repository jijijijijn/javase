package day03.loop;
/*
 * 3. for문
 * 
 * 	for(초기화변수; 조건식; 증감식;){
 * 		반복 수행할 코드
 * 	}
 * 
 */
public class Mainclass03 {
	public static void main(String[] args) {
		for(int i=0; i<10; i++) {
			System.out.println(i);
		}
		
		int i=0;
		while(1<10) {
			System.out.println(i);
			i++;
		}
		
	}

}
